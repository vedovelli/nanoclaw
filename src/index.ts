/**
 * Output handler factory for warm-pool claimed containers.
 * Mirrors processGroupMessages: strips <internal> tags, sends to channel, tracks session, resets idle.
 */
function makeWarmOutputHandler(
  chatJid: string,
  group: RegisteredGroup,
  channel: Channel,
  previousCursor: string,
): (output: ContainerOutput) => Promise<void> {
  let idleTimer: ReturnType<typeof setTimeout> | null = null;
  let outputSentToUser = false;

  const resetIdleTimer = () => {
    if (idleTimer) clearTimeout(idleTimer);
    idleTimer = setTimeout(() => {
      logger.debug(
        { group: group.name },
        'Idle timeout, closing container stdin',
      );
      queue.closeStdin(chatJid);
    }, IDLE_TIMEOUT);
  };

  return async (output: ContainerOutput) => {
    if (output.newSessionId) {
      sessions[group.folder] = output.newSessionId;
      setSession(group.folder, output.newSessionId);
      warmPool?.updateSession(group.folder, output.newSessionId);
    }

    if (output.result) {
      const raw =
        typeof output.result === 'string'
          ? output.result
          : JSON.stringify(output.result);
      const text = raw.replace(/<internal>[\s\S]*?<\/internal>/g, '').trim();
      logger.info({ group: group.name }, `Agent output: ${raw.slice(0, 200)}`);
      if (text) {
        await channel.sendMessage(chatJid, text);
        /* ved custom */
        try {
          storeMessage({
            id: `bot-${new Date().toISOString()}-${Math.random().toString(36).slice(2)}`,
            chat_jid: chatJid,
            sender: 'assistant',
            sender_name: 'Assistant',
            content: text,
            timestamp: new Date().toISOString(),
            is_from_me: true,
            is_bot_message: true,
          });
        } catch {
          // persistence is best-effort
        }
        /* ved custom end */
        outputSentToUser = true;
      }
      resetIdleTimer();
    }

    if (output.status === 'success') {
      queue.notifyIdle(chatJid);
      await channel.setTyping?.(chatJid, false);
      if (idleTimer) clearTimeout(idleTimer);
    }

    if (output.status === 'error') {
      if (!outputSentToUser) {
        lastAgentTimestamp[chatJid] = previousCursor;
        saveState();
        logger.warn(
          { chatJid },
          'Warm container error, rolled back message cursor for retry',
        );
      } else {
        logger.warn(
          { chatJid },
          'Warm container error after output was sent, skipping cursor rollback',
        );
      }
      await channel.setTyping?.(chatJid, false);
      if (idleTimer) clearTimeout(idleTimer);
    }
  };
}
